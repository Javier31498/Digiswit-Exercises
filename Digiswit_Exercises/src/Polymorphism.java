

public class Polymorphism {

    //Three parameters are given to this method
    public void Operation(int number1, int number2, int number3) {

        int solution1 = 0;
        solution1 = number1 + number2 + number3;
        System.out.println(solution1);

    }

    //Two parameters are given to this method
    public void Operation(int number1, int number2) {

        int solution2 = 0;
        solution2 = number1 + number2;
        System.out.println(solution2);

    }
}
